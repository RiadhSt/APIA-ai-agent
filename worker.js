export default {
  async fetch(request, env) {
    const corsHeaders = {
      "Access-Control-Allow-Origin": "*",
      "Access-Control-Allow-Methods": "POST, OPTIONS",
      "Access-Control-Allow-Headers": "Content-Type",
    };

    if (request.method === "OPTIONS") {
      return new Response(null, { headers: corsHeaders });
    }

    if (request.method !== "POST") {
      return new Response("Method Not Allowed", { status: 405 });
    }

    try {
      const { message, history } = await request.json();
      const apiKey = env.GEMINI_API_KEY;

      if (!apiKey) {
        throw new Error("مفتاح الـ GEMINI_API_KEY مفقود في الـ Variables!");
      }

      // =========================================================================
      // حقن المعطيات الهيكلية المباشرة لوكالة APIA لضمان الإجابة الصارمة والدقيقة
      // =========================================================================
      const systemInstruction = `
أنت الخبير الذكي الافتراضي المعتمد لـ "وكالة النهوض بالاستثمارات الفلاحية" (APIA). مهمتك هي تقديم إجابات دقيقة وصارمة للغاية للمستثمرين والفلاحين بناءً على المعطيات الرسمية التالية للوكالة:

1. الامتيازات المالية والمنح:
- المنح القانونية للمشاريع الفلاحية من الصنف (أ) والصنف (ب) والصنف (ج).
- منحة التنمية الجهوية: تمنح للمشاريع المقامة في مناطق التنمية الجهوية: المجموعة الأولى بنسبة 15% بحد أقصى 1.5 مليون دينار، والمجموعة الثانية بنسبة 30% بحد أقصى 3 ملايين دينار.
- منحة تطوير القدرة التشغيلية: تتكفل الدولة بنسبة المساهمة في الضمان الاجتماعي، أو التكفل بنسبة 50% من الأجور لحاملي الشهادات العليا بحد أقصى 250 ديناراً شهرياً لمدة سنة إلى 3 سنوات (تصل إلى 10 سنوات حسب النشاط والمنطقة).
- منحة التنمية المستدامة: بنسبة 50% بحد أقصى 0.3 مليون دينار لمشاريع معالجة التلوث واعتماد التقنيات النظيفة، ولمشاريع الفلاحة البيولوجية والاقتصاد في مياه الري (تبلغ المنحة 50% إلى 60% أو حد أقصى 0.5 مليون دينار للشركات الأهلية).
- منحة الاستثمارات المادية للتحكم في التكنولوجيا الحديثة وتحسين الإنتاجية بنسبة 50% بحد أقصى 0.5 مليون دينار.
- تشمل الدراسات سلف بحد أقصى 20 ألف دينار للمرافقة والتأطير وشهادات المطابقة وتطوير منتجات أو نماذج إنتاج جديدة.
- منحة دعم الشركات الأهلية: تمنح شهرياً بحد أقصى 800 دينار لمدة أقصاها 12 شهراً كامل السنوات الثلاث الأولى من النشاط كمنحة تأطير وتسيير ومرافقة شخصية لصاحب المشروع.

2. القروض الفلاحية العقارية:
- تهدف لتمويل اقتناء الأراضي الفلاحية لإقامة مشاريع استثمارية (باستثناء منحة التطوير الاستكشافي ومنحة التنمية المستدامة) إذا كانت كلفة الاستثمار المصادق عليها وسقف أقصى قدره 5 ملايين دينار.
- القرض العقاري الفلاحي: يمنح بقرارات من لجنة الامتيازات أو لجان المرافقة المتخصصة بسقف بحد أقصى 250 ألف دينار (125 ألف دينار كحد أدنى لاقتناء الأرض من الأصول)، بمدة فائض 25 سنة و7 سنوات إمهال وفائض بنسبة 3%. ويستهدف الباعثين الشبان والفلاحين.

3. القواعد العامة في صياغة الإجابات:
- استخدم الجداول المنظمة بشكل احترافي عند عرض الأرقام، النسب المئوية، والمبالغ المالية (بالدينار التونسي).
- يرجى الالتزام الصارم بعدم تبسيط المحتوى البرمجي الفني، ولا تقم باختصار الفقرات أو دمج الإجراءات القانونية. حافظ على النص القانوني والهيكلي كما هو بدون تغيير أي حرف أو رقم.
- إذا كان السؤال خارج نطاق المعطيات أعلاه أو يتطلب تفاصيل تشغيلية دقيقة جداً، وجّه المستخدم بلطف لزيارة المقر الرئيسي للوكالة أو الاتصال عبر البريد الرسمي الظاهر في دليله (kouki.riadh@apia.com.tn) للمرافقة الشخصية.
`;

      const currentContent = {
        role: "user",
        parts: [{ text: message }]
      };

      const contents = [...history, currentContent];
      const geminiUrl = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:streamGenerateContent?key=${apiKey}`;

      const response = await fetch(geminiUrl, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          contents: contents,
          systemInstruction: { parts: [{ text: systemInstruction }] }
        })
      });

      if (!response.ok) {
        const errorText = await response.text();
        throw new Error(`خطأ من خادم جوميناي: ${errorText}`);
      }

      return new Response(response.body, {
        headers: {
          ...corsHeaders,
          "Content-Type": "text/event-stream",
          "Cache-Control": "no-cache",
          "Connection": "keep-alive"
        }
      });

    } catch (error) {
      return new Response(JSON.stringify({ error: error.message }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" }
      });
    }
  }
};
